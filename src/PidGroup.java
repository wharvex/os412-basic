public class PidGroup {
  private final int pid1;
  private final int pid2;
  private final int pid3;

  public PidGroup(int pid1, int pid2, int pid3) {
    this.pid1 = pid1;
    this.pid2 = pid2;
    this.pid3 = pid3;
  }

  public int getPid1() {
    return pid1;
  }

  public int getPid2() {
    return pid2;
  }

  public int getPid3() {
    return pid3;
  }
}
